import { SignInButton, UserButton, useUser } from "@clerk/nextjs";
import Head from "next/head";
import { type RouterOutputs, api } from "~/utils/api";
import { dark } from "@clerk/themes";
import Image from "next/image";

import { LoadingSpinner } from "~/components/loading";
import dayjs from "dayjs";
import relativeTime from "dayjs/plugin/relativeTime"
import { MyButton } from "~/components/custombutton";
dayjs.extend(relativeTime);



// section with sort by, filter, and create post button
const PostsManager = () => {
  const user = useUser();

  // TODO: ADD SORT METHOD 
  if (!!user.isSignedIn) {
    return (
      <MyButton name=" + Create Post" />
    )
  } else if (!user.isSignedIn) {
    return (
      <span>You must be signed in to create a post!</span>
    )
  }

};




type PostWithUser = RouterOutputs["posts"]["getAll"][number];
//creates individual post view
const PostView = (props: PostWithUser) => {
  const { post, author } = props;
  return (
    <div key={post.id} className="p-8 border-b flex flex-row gap-4">
      <div>
        <Image
          src={author.profileImageUrl}
          alt="pfp"
          className="rounded-full w-12 h-12"
          width={48}
          height={48}
        />
      </div>
      <div>
        <div className="text-xl font-semibold">
          <span>{post.title}</span>
        </div>
        <div className="flex flex-row gap-1">
          <span className="font-semibold">@{author.username} Â· </span>
          <span className="font-thin">{`  ${dayjs(post.createdAt).fromNow()}`}</span>
        </div>
      </div>
    </div>
  )

}




// section with all posts
const FrontPage = () => {

  const { data } = api.posts.getAll.useQuery();

  if (!data) return <LoadingSpinner />;

  return (
    <div className="flex flex-col">
      {data?.map((fullPost) => (
        <PostView {...fullPost} key={fullPost.post.id} />)) ?? <LoadingSpinner />}
    </div>
  )
};




export default function Home() {

  const { user, isLoaded: userLoaded, isSignedIn } = useUser();

  // start fetching posts
  api.posts.getAll.useQuery();

  // return empty div if user not loaded
  if (!userLoaded) return <LoadingSpinner />;



  return (
    <>
      <Head>
        <title>kbdspace</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>


      <main className="flex justify-center h-screen">
        <div className="w-full md: max-w-5xl border-x">
          <div className="flex flex-row border-b p-4">
            <div className="w-full flex self-center">
              <h1 className="text-2xl font-bold tracking-widest">kbdspace</h1>
            </div>
            <div className="flex w-full justify-end">
              <div className="flex flex-row gap-3">
                <div className="flex flex-col text-right">
                  {!!isSignedIn && <span className="font-semibold">{user.fullName}</span>}
                  {!!isSignedIn && <span className="font-light">@{user.username}</span>}
                </div>
                <div className="flex self-center">
                {!isSignedIn && <SignInButton />}
                {!!isSignedIn && <UserButton
                  appearance={{
                    baseTheme: dark,
                    elements: {
                      avatarBox: 
                        "w-12 h-12",
                    }
                  }}
                  userProfileMode="navigation"
                  userProfileUrl={
                    typeof window !== "undefined"
                      ? `${window.location.origin}/profile`
                      : undefined
                  }
                />}
                </div>
                
              </div>

            </div>
          </div>
          <div className="flex flex-row border-b p-4 justify-end">
            <PostsManager />
          </div>
          <div>
            <FrontPage />
          </div>
        </div>
      </main>
    </>
  );
}
