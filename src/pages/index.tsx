// section with all posts
const FrontPage = () => {

  const { data } = api.posts.getAll.useQuery();

  if (!data) return (<LoadingSpinner />);


  return (
    <div className="flex flex-col gap-4">
      {data?.map((fullPost) => (
        <PostView {...fullPost} key={fullPost.post.id} />))}
    </div>
  )
};




